    def render_scene(self, scene_idx: int, scene: dict, frame_num: int, total_frames: int) -> Image.Image:
        """Render a single frame for a scene."""
        t = frame_num / max(total_frames, 1)
        visual_type = scene["visual"]
        
        # Create base image
        img = Image.new('RGB', (self.width, self.height), DARK)
        draw = ImageDraw.Draw(img)
        
        # Draw animated background
        self._draw_background(draw, t, frame_num, scene_idx)
        
        # Draw scene-specific content
        if visual_type == "title":
            self._draw_title(draw, t, scene["id"])
        elif visual_type == "problem":
            self._draw_problem(draw, t, scene["id"])
        elif visual_type == "meet":
            self._draw_meet(draw, t, scene["id"])
        elif visual_type == "workflow":
            self._draw_workflow(draw, t, scene["id"], scene_idx)
        elif visual_type == "agents":
            self._draw_agents(draw, t, scene["id"])
        elif visual_type == "capabilities":
            self._draw_capabilities(draw, t)
        elif visual_type == "projects":
            self._draw_projects(draw, t)
        elif visual_type == "closing":
            self._draw_closing(draw, t)
        
        # Draw progress bar
        self._draw_progress(draw, t, scene_idx)
        
        return img
    
    def _draw_background(self, draw, t, frame_num, scene_idx):
        """Animated background with floating accent elements."""
        # Main dark background
        draw.rectangle([(0, 0), (self.width, self.height)], fill=DARK)
        
        # Subtle accent circles (solid, fast to draw)
        colors = [PURPLE, BLUE, PINK]
        ci = colors[scene_idx % 3]
        for i in range(3):
            cx = self.width * (0.15 + 0.7 * (i / 2))
            cy = self.height * (0.2 + 0.6 * math.sin(t * 0.8 + i * 2.0))
            r = 60 + 20 * math.sin(t * 1.5 + i * 1.3)
            draw.ellipse(
                [cx - r, cy - r, cx + r, cy + r],
                fill=(ci[0]//6, ci[1]//6, ci[2]//6),
                outline=(ci[0]//4, ci[1]//4, ci[2]//4),
                width=1
            )
        
        # Small floating dots
        for i in range(15):
            sx = (self.width * ((i * 0.23 + t * 0.02) % 1.0))
            sy = (self.height * ((i * 0.47 + t * 0.01) % 1.0))
            ss = 1.5 + 1.0 * math.sin(t * 0.5 + i)
            dot_color = colors[i % 3]
            br = max(1, min(255, int(dot_color[0] * 0.15)))
            bg = max(1, min(255, int(dot_color[1] * 0.15)))
            bb = max(1, min(255, int(dot_color[2] * 0.15)))
            draw.ellipse(
                [sx - ss, sy - ss, sx + ss, sy + ss],
                fill=(br, bg, bb)
            )
    
    def _draw_text_animated(self, draw, text, x, y, font, color, t, delay=0.0, char_delay=0.03):
        """Per-character animation with staggered reveal."""
        chars_per_second = 35
        total_chars = len(text)
        reveal_time = delay + total_chars * char_delay
        
        visible_count = int(min(total_chars, (t - delay) / char_delay)) if t > delay else 0
        visible_count = max(0, min(visible_count, total_chars))
        
        shown = text[:visible_count]
        remaining = text[visible_count:]
        
        # Draw shown text
        if shown:
            bbox = draw.textbbox((0, 0), shown, font=font)
            draw.text((x, y), shown, font=font, fill=color)
        
        # Draw remaining in dim
        if remaining:
            bbox = draw.textbbox((0, 0), shown, font=font)
            rx = x + (bbox[2] - bbox[0])
            draw.text((rx, y), remaining, font=font, fill=(40, 40, 60))
        
        return visible_count / max(total_chars, 1)
    
